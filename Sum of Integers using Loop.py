# Task 2: Sum of Integers from 1 to 50 Using a Loop
# This program calculates the sum of all numbers from 1 to 50

# Step 1: Initialize a variable to store the sum
total_sum = 0

# Step 2: Use a for loop to iterate from 1 to 50
for number in range(1, 51):  # range(1, 51) gives numbers 1 to 50

# Step 3: Add each number to the total sum
    total_sum = total_sum + number

# Step 4: Display the final sum
print(f"The sum of integers from 1 to 50 is: {total_sum}")
